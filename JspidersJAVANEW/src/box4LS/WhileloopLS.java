package box4LS;

public class WhileloopLS {
	public static void main(String[] args) {
//WAJP to print 1 to 10 using while loop
		int i = 1;
		while (i <= 10) {
			System.out.println(i);
			i++;
		}
		System.out.println("-------------------------");
		// WALP to print 20 to 1 using while loop

		int i1 = 20;
		while (i1 >= 1) {
			System.out.println(i1);
			i1--;
		}
		System.out.println("-----------------");
		// WAJP to print even no. from 20 to 30 using while loop

		int n = 20;
		while (n <= 30) {
			System.out.println(n);
			n++;
		}

	}
}
